def display_image_fast(oled, img):
    oled.fill(0)
    buffer = oled.buffer
    img_len = len(img)
    buffer_len = len(buffer)
    
    # 确保我们不会超出缓冲区的范围
    copy_len = min(img_len, buffer_len)
    
    # 使用bytearray提高兼容性
    if isinstance(img, (bytes, bytearray)):
        buffer[:copy_len] = img[:copy_len]
    else:
        # 如果img不是字节类型，我们逐字节复制
        for i in range(copy_len):
            buffer[i] = img[i] & 0xFF  # 确保每个值都是一个字节
    
    oled.show()


